'''
Във файл, който се казва user_to_user.py, напишете програма, която:

Чете едно число от потребителя - a
Чете второ число от потребителя - b
Принти на екрана всички цели числa в интервала [a, b]
Програмата съобразява кое от двете числа е по-малко. При вход a = 10, b = 5, 
трябва да се изпринтят всички числа от [5, 10]
'''


a = input("Enter a: ")
a = int(a)

b = input("Enter b: ")
b = int(b)

if a > b:
	while a >= b:
		print(b)
		b += 1
else:
	while b >= a:
		print(a)
		a += 1